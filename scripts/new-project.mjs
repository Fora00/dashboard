function syncTemplate({ id, Name, thingsCamel, remoteTable }) {
  return `import { db, type OutboxPayload, type OutboxTable } from './db'
import { createCloudSync, type TableSync } from './cloudSync'
import { useSyncStatus } from './useSyncStatus'

// Generated by scripts/new-project.mjs from the pattern in src/lib/todoSync.ts
// (THE reference integration — see docs/NEW_PROJECT.md). Local-first sync for
// the ${id} project, built on the generic engine in cloudSync.ts.
//
// NOTE: until you complete docs/NEW_PROJECT.md step 3 (move ${Name}Item into
// src/lib/db.ts as a real typed table, and add '${remoteTable}' /
// ${Name}Item to the OutboxTable / OutboxPayload unions there), this file
// can't be *statically* known by the compiler to be one of the sync engine's
// known tables/payloads. The two \`as unknown as\` casts below are scaffolding
// for that gap — delete them once the db.ts edits land, at which point
// '${remoteTable}' and ${Name}Item will satisfy OutboxTable/OutboxPayload for
// real and \`db.table('${thingsCamel}')\` can become \`db.${thingsCamel}\`.

export interface ${Name}Item {
  id: string
  text: string
  createdAt: number
  updatedAt: number
}

interface ${Name}Row {
  id: string
  text: string
  created_at: number
  updated_at: number
}

const REMOTE = '${remoteTable}' as unknown as OutboxTable

const ${thingsCamel}Table: TableSync<${Name}Item, ${Name}Row> = {
  remote: REMOTE,
  table: () => db.table<${Name}Item, string>('${thingsCamel}'),
  columns: 'id, text, created_at, updated_at',
  realtime: true,
  updatedAt: (t) => t.updatedAt,
  toRow: (t) => ({
    id: t.id,
    text: t.text,
    created_at: t.createdAt,
    updated_at: t.updatedAt,
  }),
  fromRow: (r) => ({
    id: r.id,
    text: r.text,
    createdAt: Number(r.created_at),
    updatedAt: Number(r.updated_at),
  }),
}

const engine = createCloudSync({
  projectId: '${id}',
  tables: [${thingsCamel}Table],
})

// --- Local mutations (used by the UI; safe with or without sync) -----------

export async function add${Name}(text: string): Promise<void> {
  const now = Date.now()
  const item: ${Name}Item = {
    id: crypto.randomUUID(),
    text,
    createdAt: now,
    updatedAt: now,
  }
  await engine.upsert(REMOTE, item as unknown as OutboxPayload)
}

export async function delete${Name}(id: string): Promise<void> {
  await engine.remove(REMOTE, id)
}

// --- Sync engine ------------------------------------------------------------

export const flush = engine.flush
export const syncNow = engine.syncNow

/** The sync engine instance — pass to <SyncCard sync={sync} /> for status UI. */
export const sync = engine

/** Bound React hook: this project's live SyncStatus. */
export const useStatus = () => useSyncStatus(engine)

/** Start syncing (call when a session exists). Returns a stop function. */
export const start${Name}Sync = engine.start
`
}

function sqlTemplate({ id, remoteTable }) {
  return `-- ${id}: generated by scripts/new-project.mjs. Columns mirror the Dexie shape
-- in src/lib/db.ts (once you've moved the placeholder type there per
-- docs/NEW_PROJECT.md step 3); timestamps are epoch milliseconds (bigint) to
-- match the client.

create table public.${remoteTable} (
  id uuid primary key,
  text text not null,
  created_at bigint not null,
  updated_at bigint not null
);

alter table public.${remoteTable} enable row level security;

-- One policy for all ops. WITH CHECK on the write side is required so a guest
-- can't insert/update rows for a project she isn't a member of.
create policy "members full access" on public.${remoteTable}
  for all
  using (public.is_member('${id}'))
  with check (public.is_member('${id}'));

-- Realtime, so edits reach other devices live.
alter publication supabase_realtime add table public.${remoteTable};
`
}

// --- manual-step TODO printer -------------------------------------------------

function detectNextDbVersion(dbTsSource) {
  const matches = [...dbTsSource.matchAll(/db\.version\((\d+)\)/g)].map((m) => Number(m[1]))
  const highest = matches.length > 0 ? Math.max(...matches) : 0
  return highest + 1
}

