#!/usr/bin/env node
// Stamps out the files for a new dashboard subproject, automating steps 4-6
// of docs/NEW_PROJECT.md (the sync wrapper, the SQL migration, the page). The
// remaining steps (registry entry, route, db.ts edits) touch files that
// already exist and are too fragile to rewrite generically, so this script
// prints them as paste-ready TODOs instead of editing anything.
//
// Usage: npm run new-project -- <id> [--synced] [--name <Name>] [--emoji <emoji>]
// (the "--" is required so npm forwards the flags instead of eating them)

import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = join(__dirname, '..')

function usage() {
  console.log(`Usage: npm run new-project -- <id> [--synced] [--name <Name>] [--emoji <emoji>]

  IMPORTANT: the "--" before <id> is required. Without it, npm itself
  swallows "--synced"/"--name"/"--emoji" as (unknown) npm config flags
  instead of passing them to this script — you'll silently get a local-only
  page with the default 📦 emoji and no error beyond an "npm warn".

  <id>        kebab-case project id, e.g. "reading" (required)
  --synced    also generate a cloud-sync wrapper + SQL migration
  --name      override the derived PascalCase component name (default: derived from <id>)
  --emoji     override the default placeholder emoji (default: 📦)

Generates (never overwrites, never edits an existing file):
  src/projects/<id>/<Name>.tsx
  src/lib/<id>Sync.ts                 (only with --synced)
  supabase/migrations/<timestamp>_<id>.sql   (only with --synced)

Everything else (registry entry, route, db.ts edits) is printed as a
paste-ready TODO list — see docs/NEW_PROJECT.md for the full 7-step recipe
this automates (steps 4-6 are generated, 1/2/3/7 are printed/manual).`)
}

// --- arg parsing -------------------------------------------------------------

function parseArgs(argv) {
  let synced = false
  let name = null
  let emoji = null
  const positional = []
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i]
    if (a === '--synced') {
      synced = true
    } else if (a === '--name') {
      name = argv[++i]
    } else if (a === '--emoji') {
      emoji = argv[++i]
    } else if (a.startsWith('--')) {
      console.error(`error: unknown flag "${a}"`)
      process.exit(1)
    } else {
      positional.push(a)
    }
  }
  return { id: positional[0], synced, name, emoji }
}

// --- naming helpers ----------------------------------------------------------

const KEBAB_RE = /^[a-z][a-z0-9]*(-[a-z0-9]+)*$/

function toPascalCase(id) {
  return id
    .split('-')
    .filter(Boolean)
    .map((w) => w[0].toUpperCase() + w.slice(1))
    .join('')
}

function toCamelCase(id) {
  const p = toPascalCase(id)
  return p[0].toLowerCase() + p.slice(1)
}

// Naive English pluralizer for table/column names derived from the project
// id. Only appends "s" when the word doesn't already look plural — without
// this, an id whose last word is already plural (e.g. "book-ideas",
// "boardgame-ideas") gets double-pluralized ("bookIdeass", "book_ideass").
function pluralize(word) {
  return /s$/i.test(word) ? word : `${word}s`
}

function utcTimestamp() {
  const d = new Date()
  const pad = (n) => String(n).padStart(2, '0')
  return (
    `${d.getUTCFullYear()}${pad(d.getUTCMonth() + 1)}${pad(d.getUTCDate())}` +
    `${pad(d.getUTCHours())}${pad(d.getUTCMinutes())}${pad(d.getUTCSeconds())}`
  )
}

