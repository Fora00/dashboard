// --- main ---------------------------------------------------------------------

function main() {
  const { id, synced, name, emoji: emojiArg } = parseArgs(process.argv.slice(2))

  if (!id) {
    usage()
    process.exit(1)
  }

  if (!KEBAB_RE.test(id)) {
    console.error(`error: "${id}" is not kebab-case (expected e.g. "reading", "meal-planner")`)
    process.exit(1)
  }

  const projectDir = join(ROOT, 'src', 'projects', id)
  if (existsSync(projectDir)) {
    console.error(`error: src/projects/${id} already exists`)
    process.exit(1)
  }

  const Name = name || toPascalCase(id)
  const emoji = emojiArg || '📦'
  const thingsCamel = pluralize(toCamelCase(id))
  const remoteTable = pluralize(id.replace(/-/g, '_'))

  mkdirSync(projectDir, { recursive: true })
  const pagePath = join(projectDir, `${Name}.tsx`)
  writeFileSync(
    pagePath,
    synced
      ? pageTemplateSynced({ id, Name, emoji, thingsCamel })
      : pageTemplateLocal({ id, Name, emoji, thingsCamel }),
  )
  console.log(`created ${pagePath.replace(ROOT + '/', '')}`)

  let syncPath = null
  let sqlPath = null
  if (synced) {
    syncPath = join(ROOT, 'src', 'lib', `${toCamelCase(id)}Sync.ts`)
    if (existsSync(syncPath)) {
      console.error(`error: src/lib/${toCamelCase(id)}Sync.ts already exists`)
      process.exit(1)
    }
    writeFileSync(syncPath, syncTemplate({ id, Name, thingsCamel, remoteTable }))
    console.log(`created ${syncPath.replace(ROOT + '/', '')}`)

    const migrationsDir = join(ROOT, 'supabase', 'migrations')
    mkdirSync(migrationsDir, { recursive: true })
    sqlPath = join(migrationsDir, `${utcTimestamp()}_${id}.sql`)
    writeFileSync(sqlPath, sqlTemplate({ id, remoteTable }))
    console.log(`created ${sqlPath.replace(ROOT + '/', '')}`)
  }

  const dbTsPath = join(ROOT, 'src', 'lib', 'db.ts')
  const nextVersion = detectNextDbVersion(readFileSync(dbTsPath, 'utf8'))

  printTodos({ id, Name, emoji, synced, thingsCamel, remoteTable, nextVersion })
}

main()
